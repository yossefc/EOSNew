// Fonction de formatage des adresses
export const formatAddress = (address) => {
    if (!address) return address;
    return address.replace(/\s+/g, ' ').trim().toUpperCase();
};

// Fonction de validation du code postal franÃ§ais
export const isValidFrenchPostalCode = (postalCode) => {
    return /^(0[1-9]|[1-8]\d|9[0-8])\d{3}$/.test(postalCode);
};